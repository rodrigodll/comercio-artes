            <p>Embora tenha, a Maçonaria brasileira, se iniciado em 1796 com a Loja Areópago de Itambé fundada por Manoel Arruda Câmara, ex-frade carmelita, na raia das províncias de Pernambuco e Paraíba, e em 1797 com a Loja Cavaleiros da Luz, criada na povoação da Barra, em Salvador, Bahia, e ainda com a Loja União, em 1800, sucedida pela Loja Reunião em 1802, no Rio de Janeiro, só em 1822, quando a campanha pela independência do Brasil se tornava mais intensa, é que iria ser criada sua primeira Obediência, com Jurisdição nacional, exatamente com a incumbência de levar a cabo o processo de emancipação política do país.</p>

            <p>Criado em 17 de junho de 1822, por três Lojas do Oriente do Rio de Janeiro, a Commercio e Artes na Idade do Ouro (fundada em 15/11/1815), a União e Tranqüilidade; e a Esperança de Niterói (resultantes da divisão da primeira e fundadas em 21/06/1822), o Grande Oriente Brasileiro teve, como seus primeiros mandatários José Bonifácio de Andrada e Silva, ministro do Reino e de Estrangeiros e Joaquim Gonçalves Ledo, Primeiro Vigilante.</p>
            
            <p>Em 2 de agosto de 1822, por proposta de José Bonifácio, é iniciado o Príncipe Regente, D. Pedro I, adotando o nome histórico de Guatimozim, e passa a fazer parte do Quadro de Obreiros da Loja "Comércio e Artes". No dia 5 de agosto, por proposta de Gonçalves Ledo, que ocupava a presidência dos trabalhos, foi aprovada a Exaltação ao Grau de Mestre Maçom que possibilitou, posteriormente, em 4 de outubro de 1822, numa jogada política de Ledo, o Imperador ser eleito e empossado no cargo de Grão-Mestre, do Grande Oriente do Brasil, substituindo José Bonifácio.</p>

            <p>D.Pedro I, após a Independência do Brasil, em <b>1822</b>, governa o império aos trancos e barrancos até <b>1831</b>, quando, instado pelos graves fatos ocorridos em Portugal (a deposição de sua filha e a usurpação do trono português por seu irmão, D. Miguel e paralelamente, pressionado pela insatisfação popular capitaneada pelos Liberais, que temiam um retrocesso no Brasil), <b>abdica do trono brasileiro em 07/04/1831</b>, em favor de seu filho, então com 5 anos de idade. Inicia-se então, o período mais turbulento de nossa História, a Regência.</p>

            <p>D.Pedro parte para Portugal em 1831 e nunca mais põe os pés no Brasil. Em <b>23/11/1831, retorna as atividades o Grande Oriente do Brasil</b> (entidade sucessora do Grande Oriente Brasiliano extinto por D.Pedro I em 21/10/1822). Em <b>1834</b> D.Pedro I morre em Portugal</p>

            <p>As Lojas Comercio e Artes, União e Tranquilidade e Esperança de Niterói funcionavam nesta época na Rua Conde d'Eu (depois rua do Conde, atual Mem de Sá), pois o terreno onde localiza-se hoje o Palácio Maçônico do Lavradio foi comprado em <b>1836</b> pelo ator português Vítor Porfinio de Borjas, para nele construir o teatro de sua companhia; não conseguindo terminar a obra por falta de recursos, cedeu o prédio às Lojas Maçônicas em <b>1840</b>. Até este momento, o Palácio encontrava-se sendo erguido, ainda como um futuro teatro, sendo então adaptado ao novo projeto de tornar-se o maior centro de Lojas Maçônicas do RJ e do Brasil.</p>

            <p>Somente em junho de <b>1842</b>, com a inauguração do Palácio Maçônico, as lojas que fundaram o Grande Oriente do Brasil, dentre outras, passaram a funcionar na Rua do Lavradio nº 97, mesmo período em que ocorreu a instalação do GOB neste mesmo prédio.</p>

            <p>Para simples efeito informativo, as Lojas passaram a ocupar o prédio do Lavradio, depois de perambular por vários endereços: Rua Nova do Conde, Rua do Lavradio (em outra casa, no começo da rua), Rua Nova do Ouvidor, Rua de S. José e Rua dos Ciganos.</p>

            <p>Diante dos fatos narrados, expomos abaixo uma “linha do tempo”, onde podemos afirmar com toda certeza que: D. Pedro I nunca entrou no Palácio do Lavradio seja para iniciar ou para assistir qualquer outro tipo de reunião.</p>



